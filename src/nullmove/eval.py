def evaluate(board: chess.Board) -> int:
    """Returns centipawns from the side-to-move perspective."""
    dll = try_load_default()
    if dll is not None:
        # Only use DLL for normal positions; python handles mate/draw rules.
        if board.is_checkmate():
            return -100_000
        if board.is_stalemate() or board.is_insufficient_material() or board.can_claim_draw():
            return 0
        return dll.evaluate(board)

    if board.is_checkmate():
        # side to move is mated
        return -100_000
    if board.is_stalemate() or board.is_insufficient_material() or board.can_claim_draw():
        return 0

    score_white = 0
    for piece_type in PIECE_VALUE:
        value = PIECE_VALUE[piece_type]
        pst = PST[piece_type]

        for sq in board.pieces(piece_type, chess.WHITE):
            score_white += value
            score_white += pst[sq]

        for sq in board.pieces(piece_type, chess.BLACK):
            score_white -= value
            score_white -= pst[chess.square_mirror(sq)]

    # Convert to side-to-move perspective
    return score_white if board.turn == chess.WHITE else -score_white
